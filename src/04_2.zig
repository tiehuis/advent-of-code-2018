const std = @import("std");

const max_guard_id = 4096;
const count_index = 60;

pub fn main() void {
    var guards = [][61]usize{[]usize{0} ** 61} ** max_guard_id;

    {
        var id: usize = 0;
        var i: usize = 0;
        while (i < events.len) : (i += 1) {
            const e = events[i];

            if (e.begin > 0) {
                id = @intCast(usize, e.begin);
            } else {
                std.debug.assert(e.begin == fall_asleep);

                var j: usize = e.minute;
                while (j < events[i + 1].minute) : (j += 1) {
                    guards[id][j] += 1;
                    guards[id][count_index] += 1;
                }

                i += 1;
            }
        }
    }

    var max_m: usize = 0;
    var max_i: usize = 0;
    var max_g_id: usize = 0;

    var i: usize = 0;
    while (i < 60) : (i += 1) {
        for (guards) |g, j| {
            if (g[i] >= max_m) {
                max_m = g[i];
                max_i = i;
                max_g_id = j;
            }
        }
    }

    std.debug.warn("{}\n", max_g_id * max_i);
}

